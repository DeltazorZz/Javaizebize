
package idomok;

public class Gomb extends FaIdom{
       private double sugar;

    public Gomb(double sugar) {
        this.sugar = sugar;
    }

    @Override
    public String toString() {
        return "Gomb{" + "sugar=" + sugar + '}';
    }

    @Override
    public double terfogat() {
        return super.terfogat();
    }
       
    
}
